public class Main
{
    public static void main(String[] args)
    {
        int numOne = 22;
        int numTwo = 12;

        if(numOne < numTwo)
        {
            System.out.println(numOne+" is lesser than "+numTwo);
        }
        else if(numOne == numTwo)
        {
            System.out.println(numOne+" is equal to "+numTwo);
        }
        else if(numOne > numTwo)
        {
            System.out.println(numOne+" is greater than "+numTwo);
        }
    }
}